// Literais são uma forma de anotar valores específicos (dados) no código do programa. Literais são encontrados em praticamente todas as linguagens de programação, incluindo JavaScript. Usamos literais no capítulo anterior ao inicializar variáveis.
let year = 1990;
console.log(year); // -> 1990
console.log(1991); // -> 1991
console.log("Alice"); // -> Alice
